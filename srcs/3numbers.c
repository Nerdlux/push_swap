void	ft_3num(t_struct *t)
{
	if (t->a[0] > t->a[1] && t->a[1] < t->a[2] && t->a[0] < t->a[2])
		ft_sa();
	if (t->a[0] > t->a[1] && t->a[1] > t->a[2] && t->a[0] < t->a[2])
	{
		ft_sa();
		ft_rra();
	}
	if (t->a[0] > t->a[1] && t->a[1] < t->a[2] && t->a[0] > t->a[2])
		ft_ra();
	if (t->a[0] < t->a[1] && t->a[1] > t->a[2] && t->a[0] > t->a[2])
	{
		ft_sa();
		ft_ra();
	}
	if (t->a[0] < t->a[1] && t->a[1] > t->a[2] && t->a[0] < t->a[2])
		ft_rra();
}
