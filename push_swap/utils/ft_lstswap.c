#include "../push_swap.h"


void	ft_lstswap(t_list **s)
{
	t_list	*tmp;

	if (*s != NULL && (*s)->next != NULL)
	{
		tmp = (*s)->next;
		(*s)->next = tmp->next;
		tmp->next = *s;
		*s = tmp;
	}
}
